import { useState, useEffect, useCallback } from 'react';
import { statsService } from '../services/statsService';
import { useAuth } from '../contexts/AuthContext';
import { UserStats } from '../types';

export function useStats() {
  const { user } = useAuth();
  const [stats, setStats] = useState<UserStats | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  const fetchStats = useCallback(async () => {
    if (!user) {
      setStats(null);
      setLoading(false);
      return;
    }

    try {
      setLoading(true);
      setError(null);
      const userStats = await statsService.getUserStats(user.id);
      setStats(userStats);
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Failed to fetch stats');
      console.error('Failed to fetch user stats:', err);
    } finally {
      setLoading(false);
    }
  }, [user]);

  useEffect(() => {
    fetchStats();
  }, [fetchStats]);

  const refreshStats = useCallback(async () => {
    await fetchStats();
  }, [fetchStats]);

  const getWeeklyStats = useCallback(async () => {
    if (!user) return null;

    try {
      return await statsService.getWeeklyStats(user.id);
    } catch (err) {
      console.error('Failed to fetch weekly stats:', err);
      return null;
    }
  }, [user]);

  const getMonthlyStats = useCallback(async () => {
    if (!user) return null;

    try {
      return await statsService.getMonthlyStats(user.id);
    } catch (err) {
      console.error('Failed to fetch monthly stats:', err);
      return null;
    }
  }, [user]);

  return {
    stats,
    loading,
    error,
    refreshStats,
    getWeeklyStats,
    getMonthlyStats,
  };
}
